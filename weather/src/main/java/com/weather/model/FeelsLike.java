package com.weather.model;

public class FeelsLike {
    private Double day;
    private Double min;
    private Double max;
    private Double night;
    private Double eve;

    public Double getDay() {
        return day;
    }

    public Double getMin() {
        return min;
    }

    public Double getMax() {
        return max;
    }

    public Double getNight() {
        return night;
    }

    public Double getEve() {
        return eve;
    }

    public Double getMorn() {
        return morn;
    }

    private Double morn;
}
